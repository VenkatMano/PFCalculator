package com.apache.pfcalculator.dataprocessservice.model;

public class CalculateInput {
	
	private Double basicSalary;
	
	private Double yourContribution;
	
	private Double employerContribution;
	
	private Double retirementAge;
	
	private Double currentBalance;
	
	private Double currentInterestRate;
	
	private Double currentAge;

	public Double getBasicSalary() {
		return basicSalary;
	}

	public void setBasicSalary(Double basicSalary) {
		this.basicSalary = basicSalary;
	}

	public Double getYourContribution() {
		return yourContribution;
	}

	public void setYourContribution(Double yourContribution) {
		this.yourContribution = yourContribution;
	}

	public Double getEmployerContribution() {
		return employerContribution;
	}

	public void setEmployerContribution(Double employerContribution) {
		this.employerContribution = employerContribution;
	}

	public Double getRetirementAge() {
		return retirementAge;
	}

	public void setRetirementAge(Double retirementAge) {
		this.retirementAge = retirementAge;
	}

	public Double getCurrentBalance() {
		return currentBalance;
	}

	public void setCurrentBalance(Double currentBalance) {
		this.currentBalance = currentBalance;
	}

	public Double getCurrentInterestRate() {
		return currentInterestRate;
	}

	public void setCurrentInterestRate(Double currentInterestRate) {
		this.currentInterestRate = currentInterestRate;
	}

	public Double getCurrentAge() {
		return currentAge;
	}

	public void setCurrentAge(Double currentAge) {
		this.currentAge = currentAge;
	}
	
	
}
